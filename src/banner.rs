pub fn show(
	env: Env,
	is_indexer: bool,
	is_server: bool,
	skip_ascii: bool,
) -> Result<()> {
	let banner = if skip_ascii {
		"".to_string()
	} else {
		format!(
			"\n{}\n\n",
			if env::var("COLORTERM").is_ok() {
				str::from_utf8(BANNER_TRUECOLOR)?
			} else {
				str::from_utf8(BANNER_ANSI)?
			}
		)
	};

	let name = "Barreleye".to_string();
	let tags = {
		let s = |s| style(s).bright().bold();

		let mut t = vec![];
		if env == Env::Localhost {
			t.push(s("+Localhost").red().to_string())
		} else if env == Env::Testnet {
			t.push(s("+Testnet").red().to_string())
		}
		if is_indexer && !is_server {
			t.push(s("+IndexerOnly").cyan().to_string())
		} else if is_server && !is_indexer {
			t.push(s("+ServerOnly").cyan().to_string())
		}

		let tags = t.join(", ");
		if tags.is_empty() {
			tags
		} else {
			format!(" [{}]", tags)
		}
	};

	println!(
		"{} {} v{}{}\n https://barreleye.com\n",
		banner,
		style(name).bold(),
		env!("CARGO_PKG_VERSION"),
		tags,
	);

	Ok(())
}
